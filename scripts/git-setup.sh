  #   gh ssh-key add ~/.ssh/id_ed25519.pub --title "\$(hostname)" --type authentication
  #   gh ssh-key add ~/.ssh/id_ed25519.pub --title "\$(hostname)" --type signing
	signingKey = ~/.ssh/id_ed25519.pub
	name = $GIT_NAME
	email = $GIT_EMAIL
